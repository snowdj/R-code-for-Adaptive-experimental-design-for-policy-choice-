library(tidyverse)

rr=40

priordata=tibble(
    covar1=rep(1:4, 3*rr),
    treatment1=rep(c(1,0,0), 4*rr),
    treatment2=rep(c(0,1,0), 4*rr),
    treatment3=rep(c(0,0,1), 4*rr),
    theta_i=.1* covar1 + .1 * treatment1 + .2 * treatment2 + .3*treatment3,
    outcome=runif(12*rr)<theta_i
)


newwave=tibble(
  covar1=rep(1:4, 3*rr)
)

write_csv(priordata, "priordata_test.csv")
write_csv(newwave, "newwave_test.csv")
